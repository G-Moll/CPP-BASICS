#include <iostream>
using namespace std;

int main() {

    /* RELATIONAL OPERATORS
        ==  Equal to
        !=  Not Equal to
        <   Less than
        <=  Less than or Equal to
        >   Greater than
        >=  Greater than or Equal to
    */

    int numOne = 10;
    int numTwo = 20;

    cout << "==: " << ( numOne == numTwo ) << endl;
    cout << "!=: " << ( numOne != numTwo ) << endl;
    cout << ">: " << ( numOne > numTwo ) << endl;
    cout << ">=: " << ( numOne >= numTwo ) << endl;
    cout << "<: " << ( numOne < numTwo ) << endl;
    cout << "<=: " << ( numOne <= numTwo ) << endl;
    // cout << "==: " << ( numOne == 10 ) << endl;
    // cout << "==: " << ( numOne == 5 + 5 ) << endl;


    return 0;
}
