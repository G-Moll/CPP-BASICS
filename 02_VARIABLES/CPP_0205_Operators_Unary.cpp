#include <iostream>
using namespace std;

int main() {

    /* UNARY OPERATORS
        ++ Increment
        -- Decrement
    */

    int numOne = 0;
    numOne++;
    cout << "numOne: " << numOne << endl;
    numOne--;
    cout << "numOne: " << numOne << endl;
    numOne--;
    cout << "numOne: " << numOne << endl;


    return 0;
}
