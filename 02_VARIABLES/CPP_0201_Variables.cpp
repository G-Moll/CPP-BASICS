#include <iostream>
using namespace std;

int main() {
    // Variables: Memory Space used for storing some data
    // Data Type / Identifier (name) / Value
    // Creating a Variables, it is called
    // Declaration
    int age;
    float price;
    double range;
    char gender;
    bool single;

    // Assign some value to Variables
    // = Assignment Operator
    // Initialize (Variables)
    age = 33;
    price = 100.50;
    range = 3.102548102548;
    gender = 'M';
    single = true;

    cout << age << endl;
    cout << price << endl;
    cout << range << endl;
    cout << gender << endl;
    cout << single << endl;

    return 0;
}
